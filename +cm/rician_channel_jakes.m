%--------------------------------------------------------------------------
%   h = rician_channel_jakes(T, fd, fs, E0, K, phi_los)
%--------------------------------------------------------------------------
%   功能:
%   基于jakes模型生成的莱斯衰落信道系数
%   输出的信号幅度服从莱斯分布，相位服从均匀分布
%--------------------------------------------------------------------------
%   输入:
%           T               信号持续时间 (单位: 秒)
%           fd              最大多普勒频移 (单位: Hz)
%           fs              采样率 (单位: Hz)
%           K               莱斯K因子，直射路径与散射路径功率比 (K越大，直射分量越强)
%           phi_los         直射路径的初始相位 (单位: rad)
%           E0              信号总功率 (可选，默认=1)
%
% 输出:
%   h                       复信道增益 (包含幅度和相位信息)
%                           幅度服从莱斯分布
%                           总功率归一化为E0
function h = rician_channel_jakes(T, fd, fs, K, phi_los, E0)
if nargin <= 6
    E0 = 1;
end
%--------------------------------------------------------------------------
% Jakes模型参数设置
% 经典Jakes模型使用N0=8时对应M=34个正弦波(2N0+2)
% 这里简化实现使用固定M=10个正弦波(实际应用中建议M>=8)
% 总平面波数量N = 4M + 2 (保证统计特性)
%--------------------------------------------------------------------------
M = 8;                                                                      % 使用的正弦波数量(建议>=8)
N = 4*M + 2;                                                                % 总平面波数量

wd = 2*pi*fd;                                                               % 角多普勒频率[rad/s]
t_axis = (0:1/fs:T-1/fs)';                                                  % 时间向量(列向量)

% 幅度设置(第一个分量幅度为sqrt(2)，其余为2)
% 保证总功率归一化
A = [sqrt(2), 2*ones(1, M)];  

% 初始相位设置(在复平面均匀分布)
phi = exp(1j*pi/(M+1)*(0:M)); 

% 多普勒频移设置(按cos规律分布)
sig = [exp(1j.*wd.*(cos(2*pi.*(0:M)./N).*t_axis))];                         %信号  其中多普勒频率按照余弦规律分布在0-pi

% 合成信号并归一化功率
% 注意: 实际实现与经典Jakes模型略有不同，但统计特性相似
h_rayleigh = E0/sqrt(2*M+1) .* sum(A.*(sig.*phi),2);
    
% 添加直射分量
los_component = sqrt(K/(K+1)) * exp(1j*(2*pi*fd*t_axis + phi_los)); 
    
% 合成莱斯信道
h = los_component + sqrt(1/(K+1)) * h_rayleigh;
end
