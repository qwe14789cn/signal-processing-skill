%--------------------------------------------------------------------------
%   h = rayleigh_channel_jakes(T, fd, fs, E0)
%--------------------------------------------------------------------------
%   功能:
%   基于Jakes模型生成瑞利衰落信道系数
%   输出信号幅度服从瑞利分布，相位服从均匀分布
%--------------------------------------------------------------------------
%   输入:
%           T               仿真时长(秒)
%           fd             最大多普勒频率(Hz)
%           fs             采样频率(Hz)
%           E0             信号功率(可选，默认为1)
%   输出:
%           h               复瑞利衰落信道系数(N×1向量)
%--------------------------------------------------------------------------
%   例子:
%   h = rayleigh_channel_jakes(0.05, 900, 1e6, 1); % 50ms时长，多普勒900Hz
%--------------------------------------------------------------------------
function h = rayleigh_channel_jakes(T,fd,fs,E0)
if nargin<=3
    E0=1;       % 默认信号功率为1
end
%--------------------------------------------------------------------------
% Jakes模型参数设置
% 经典Jakes模型使用N0=8时对应M=34个正弦波(2N0+2)
% 这里简化实现使用固定M=10个正弦波(实际应用中建议M>=8)
% 总平面波数量N = 4M + 2 (保证统计特性)
%--------------------------------------------------------------------------
M = 8;                                                                      % 使用的正弦波数量(建议>=8)
N = 4*M + 2;                                                                % 总平面波数量

wd = 2*pi*fd;                                                               % 角多普勒频率[rad/s]
t_axis = (0:1/fs:T-1/fs)';                                                  % 时间向量(列向量)

% 幅度设置(第一个分量幅度为sqrt(2)，其余为2)
% 保证总功率归一化
A = [sqrt(2), 2*ones(1, M)];  

% 初始相位设置(在复平面均匀分布)
phi = exp(1j*pi/(M+1)*(0:M)); 

% 多普勒频移设置(按cos规律分布)
wn = wd.*(cos(2*pi.*(0:M)./N));

sig = [exp(1j.*wn.*t_axis)];                                               %信号  其中多普勒频率按照余弦规律分布在0-pi

disp(8*pi/wd/(cos(2*pi.*M./N)))
% 合成信号并归一化功率
% 注意: 实际实现与经典Jakes模型略有不同，但统计特性相似
h = E0/sqrt(2*M+1) .* sum(A.*(sig.*phi),2);

end
