%--------------------------------------------------------------------------
%   Out = Tomohiko_Sakamoto(y,m,d)
%--------------------------------------------------------------------------
%   功能：
%   输入年月日返回星期
%--------------------------------------------------------------------------
%   输入:
%           y           年
%           m           月
%           d           日
%--------------------------------------------------------------------------
%   例子:
%   Out = Tomohiko_Sakamoto(2025,2,18)
%--------------------------------------------------------------------------
function Out = Tomohiko_Sakamoto(y,m,d)
t = [0 3 2 5 0 3 5 1 4 6 2 4];
y = y-(m<3);
Out = rem(y+ floor(y/4) -floor(y/100) + floor(y/400) + t(m) + d,7);
end