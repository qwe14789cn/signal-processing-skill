%--------------------------------------------------------------------------
%   [vpp] = dbm_convert(dbm,R)
%--------------------------------------------------------------------------
%   功能:
%   dbm 单位换算
%--------------------------------------------------------------------------
%   输入:
%           dbm                 功率绝对值
%           R                   阻抗匹配(默认为50Ω)
%--------------------------------------------------------------------------
function dbm_convert(dbm,R)
if nargin==1
    R=50;
end
Vpp = 2*10.^((dbm-30+10*log10(2.*R))./20);
Vp = Vpp/2;
Vrms = Vp/sqrt(2);
P_w = 10^((dbm-30)/10);
P_mw = 10^(dbm/10);
disp('----------------------------------')
disp(['阻抗 R = ' num2str(R)])
disp('----------------------------------')
fprintf('有效电压\t\t V_rms  =\t %f\n', Vrms);
fprintf('峰值电压\t\t V_p    =\t %f\n' ,Vp);
fprintf('峰峰值电压\t V_pp   =\t %f\n', Vpp);
disp('----------------------------------')
disp('阻抗 R 不影响功率换算')
disp('----------------------------------')
fprintf('功率瓦\t\t P_w    =\t %f\n',P_w);
fprintf('功率毫瓦\t\t P_mw   =\t %f\n',P_mw);

end