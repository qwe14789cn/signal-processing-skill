%--------------------------------------------------------------------------
%   wave = nlfm_sine_wave(T, Am, fm, fs)
%--------------------------------------------------------------------------
%   功能：
%       生成一个正弦调制的非线性调频（NLFM）复指数信号。
%       信号形式为：s(t) = exp(j * φ(t))，
%       其中相位 φ(t) = (Am / fm) * sin(2π * fm * t)，
%       对应的瞬时频率为 f_i(t) = Am * cos(2π * fm * t) （单位：Hz）。
%
%       此信号常用于雷达波形设计、抗干扰通信等场景，具有时变的
%       正弦频率调制特性，属于典型的非线性调频（NLFM）信号。
%
%--------------------------------------------------------------------------
%   输入参数：
%       T   - 信号持续时间（秒），标量，必须 > 0
%       Am  - 频率调制的峰值偏移（Hz），标量，必须 ≥ 0
%       fm  - 调制正弦波的频率（Hz），标量，必须 > 0
%       fs  - 采样率（Hz），标量，必须 > 0，建议满足 fs ≫ 2*(|f_i(t)|_max)
%
%   输出参数：
%       wave - 复基带 NLFM 信号，列向量，长度 N = floor(T * fs)
%
%--------------------------------------------------------------------------
%   示例：
%       fs = 500e6;                          % 采样率 500 MHz
%       sig = nlfm_sine(100e-6, 60e6, 40e3, fs);
%       spectrogram(sig, 256, 250, 256, fs, 'yaxis');
%       title('正弦调制 NLFM 信号时频图');
%       xlabel('时间 (s)'); ylabel('频率 (Hz)');
%
%--------------------------------------------------------------------------
function wave = nlfm_sine_wave(T, Am, fm, fs)

    % --- 输入参数校验 ---
    if nargin ~= 4
        error('nlfm_sine: 需要恰好 4 个输入参数 (T, Am, fm, fs)');
    end
    if ~isscalar(T) || T <= 0
        error('nlfm_sine: T 必须是正标量（信号持续时间，单位：秒）');
    end
    if ~isscalar(Am) || Am < 0
        error('nlfm_sine: Am 必须是非负标量（峰值频偏，单位：Hz）');
    end
    if ~isscalar(fm) || fm <= 0
        error('nlfm_sine: fm 必须是正标量（调制频率，单位：Hz）');
    end
    if ~isscalar(fs) || fs <= 0
        error('nlfm_sine: fs 必须是正标量（采样率，单位：Hz）');
    end

    % --- 构建时间轴（避免浮点累积误差）---
    N = floor(T * fs);              % 采样点数
    if N == 0
        warning('nlfm_sine: 信号持续时间过短或采样率过低，输出为空信号');
        wave = zeros(0, 1);
        return;
    end
    t_axis = (0:N-1).' / fs;        % 列向量，t ∈ [0, T)

    % --- 计算相位 φ(t) = (Am / fm) * sin(2π fm t) ---
    % 注意：当 fm 很小时，Am/fm 可能很大，但这是数学定义所决定的
    phi = (Am / fm) * sin(2 * pi * fm * t_axis);

    % --- 生成复指数信号 s(t) = exp(j * φ(t)) ---
    wave = exp(1j * phi);

    % -------------------------------------------------------------------------
    % 注：瞬时频率可由下式验证（用于调试）：
    %   inst_freq = [0; diff(phi)] / (2*pi) / (1/fs);  % 单位：Hz
    % 或使用更稳健的方法（考虑相位卷绕）：
    %   inst_freq = diff(unwrap(phi)) / (2*pi) / (1/fs);
    % -------------------------------------------------------------------------

end