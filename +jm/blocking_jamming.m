%--------------------------------------------------------------------------
%   noise = blocking_jamming(N)
%--------------------------------------------------------------------------
%   功能：
%   阻塞式干扰(全频带)
%--------------------------------------------------------------------------
%   输入：
%           N                   噪声长度
%   输出：
%           noise               噪声
%--------------------------------------------------------------------------

function noise = blocking_jamming(N)
noise = 1./sqrt(2)*(randn(N,1) + 1j.*randn(N,1));
end