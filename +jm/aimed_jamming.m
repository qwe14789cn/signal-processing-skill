%--------------------------------------------------------------------------
%   noise = aimed_jamming(N,k)
%--------------------------------------------------------------------------
%   功能：
%   瞄准式干扰(窄带干扰)
%--------------------------------------------------------------------------
%   输入：
%           N                   噪声长度
%           k                   带宽系数[0-1]
%   输出：
%           noise               色噪声
%--------------------------------------------------------------------------
function noise = aimed_jamming(N,k)
b = fir1(48,k);
noise = conv(1/sqrt(2).*(randn(N,1)+1j.*randn(N,1)),b);
end
