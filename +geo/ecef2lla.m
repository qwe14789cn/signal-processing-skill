%--------------------------------------------------------------------------
%   lla = ecef2lla(ECEF_XYZ)
%--------------------------------------------------------------------------
%   功能：
%   从ECEF坐标系转经纬高lla坐标系
%--------------------------------------------------------------------------
%   输入:
%           ECEF_XYZ = [x y z]
%                           其中:
%                           x				ECEF坐标系下的x
%			                y				ECEF坐标系下的y
%			                z				ECEF坐标系下的z
%   输出:
%           lla = [lat lon alt]
%           其中：
%                           lat           	纬度
%                           lon           	经度
%                           alt           	高度
%--------------------------------------------------------------------------
%   例子:
%   lla = ecef2lla([4510731,4510731,0])
%--------------------------------------------------------------------------
function lla = ecef2lla(EFEF_XYZ)
x = EFEF_XYZ(1,:);y = EFEF_XYZ(2,:);z = EFEF_XYZ(3,:);
WGS84_A = 6378137.0;                                % 长半轴 
WGS84_f = 1/298.257223565;                          % 扁率 
WGS84_E2 = WGS84_f * (2 - WGS84_f);

% 经度计算（直接反正切）
lon = atan2d(y, x);            % 单位：度 

% 初始纬度迭代（Bowring方法）
p = sqrt(x.^2 + y.^2);         % 赤道平面投影距离 
lat0 = atan2d(z, p * (1 - WGS84_E2)); % 初始估计
N0 = WGS84_A ./ sqrt(1 - WGS84_E2 .* sind(lat0).^2); % 卯酉圈半径 
alt0 = p ./ cosd(lat0) - N0;   % 初始高度 

% 迭代优化纬度（通常1-2次迭代足够）
for i = 1:5
    lat = atan2d(z, p .* (1 - WGS84_E2 * N0 ./ (N0 + alt0))); 
    N = WGS84_A ./ sqrt(1 - WGS84_E2 .* sind(lat).^2);
    alt = p ./ cosd(lat) - N;
    if max(abs(lat - lat0)) < 1e-9
        break;
    end
    lat0 = lat; alt0 = alt; N0 = N;
end


lla = [lat;lon;alt];
end